//======================pwm==========================//
void config_pwm(void)
{
	MX_TIM1_Init();//config CH1 CH2 TIM1
	MX_TIM8_Init();//config CH1 CH2 CH3 CH4 TIM8
	
	//===============TIM 1==================//
	HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_1); 
	HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_2);
	//===============TIM 8==================//
	HAL_TIM_PWM_Start(&htim8,TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim8,TIM_CHANNEL_2);
	HAL_TIM_PWM_Start(&htim8,TIM_CHANNEL_3);
	HAL_TIM_PWM_Start(&htim8,TIM_CHANNEL_4);
}
//=====================encotder===================//
void Clear_En_1(void)
{
	en1=0;
	TIM2->CNT=0;
}

void Clear_En_2(void)
{
	en2=0;
	TIM5->CNT=0;
}

void Clear_Encoder_all(void)
{
	en1=0;
	en2=0;
	TIM2->CNT=0;
	TIM5->CNT=0;
}

int32_t read_en_1(void)
{
	en1=TIM2->CNT;
	return en1;
}

int32_t read_en_2(void)
{
	en2=TIM5->CNT;
	return en2;
}


void config_encotder(void)
{
	MX_TIM2_Init();
	MX_TIM5_Init();
	
	HAL_TIM_Encoder_Start(&htim2,TIM_CHANNEL_1|TIM_CHANNEL_2);
	HAL_TIM_Encoder_Start(&htim5,TIM_CHANNEL_1|TIM_CHANNEL_2);
	Clear_Encoder_all();
}
